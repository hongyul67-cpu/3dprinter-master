// 레이어(적층) 층수 = 높이 ÷ 레이어 두께
(function(){
  const Hs=[12,15,18,20,24,30,36,40,45,48,50,60,72,75,80,90,100,120,150,160,180,200,240,250,300];
  const Ls=[0.1,0.15,0.2,0.25,0.3,0.4,0.5,0.6];
  let cnt=0;
  for(const H of Hs){
    if(cnt>=56) break;
    for(const L of Ls){
      const n=Math.round((H/L)*1000)/1000;
      if(!Number.isInteger(n)||n<20||n>3000) continue;
      if(cnt>=56) break;
      const set=[n];
      for(const x of [Math.round(n/2), n*2, n+10, Math.max(1,n-10), Math.round(n*1.5), n+1, n+25]){
        if(set.length>=4) break;
        if(x!==n && x>0 && !set.includes(x)) set.push(x);
      }
      QUESTIONS.push({t:'slice',
        q:`높이 ${H}mm인 형상을 레이어 두께 ${L}mm로 슬라이싱할 때 전체 적층 층수는?`,
        c:set.map(x=>x+'층'), a:0,
        e:`전체 층수 = 높이 ÷ 레이어 두께 = ${H} ÷ ${L} = ${n}층.`});
      cnt++;
    }
  }
})();

// 전체 높이 = 레이어 두께 × 층수
(function(){
  const Ls=[0.1,0.15,0.2,0.25,0.3,0.5];
  const ns=[50,80,100,120,150,200,240,250,300,400];
  let cnt=0;
  for(const L of Ls){
    if(cnt>=30) break;
    for(const n of ns){
      if(cnt>=30) break;
      const H=Math.round(L*n*100)/100;
      if(H<5||H>400) continue;
      const set=[H];
      for(const x of [Math.round(H/2*100)/100, Math.round(H*2*100)/100, H+10, Math.max(1,H-10), Math.round(H*1.5*100)/100]){
        if(set.length>=4) break;
        if(x!==H && x>0 && !set.includes(x)) set.push(x);
      }
      QUESTIONS.push({t:'slice',
        q:`레이어 두께 ${L}mm로 ${n}층을 적층하면 출력물의 전체 높이는?`,
        c:set.map(x=>x+'mm'), a:0,
        e:`전체 높이 = 레이어 두께 × 층수 = ${L} × ${n} = ${H}mm.`});
      cnt++;
    }
  }
})();

// 축의 최대/최소 지름 = 기준 ± 공차
(function(){
  const fmt=x=>Number(x.toFixed(3));
  const combos=[[10,0.1],[12,0.2],[15,0.1],[16,0.05],[20,0.2],[20,0.1],[24,0.3],[25,0.2],[30,0.2],[30,0.5],[32,0.1],[36,0.3],[40,0.2],[45,0.5],[48,0.2],[50,0.2],[50,0.5],[60,0.3],[64,0.2],[72,0.5],[80,0.4],[90,0.5],[100,0.5],[120,0.2]];
  for(const [D,t] of combos){
    const mx=fmt(D+t), mn=fmt(D-t), d2=fmt(D+2*t);
    QUESTIONS.push({t:'draw',
      q:`축의 기준 지름이 ${D}mm이고 공차가 ±${t}mm일 때, 축의 최대 지름은?`,
      c:[mx+'mm', mn+'mm', D+'mm', d2+'mm'], a:0,
      e:`최대 지름 = 기준 + 공차 = ${D} + ${t} = ${mx}mm.`});
  }
  for(const [D,t] of combos){
    const mx=fmt(D+t), mn=fmt(D-t), d2=fmt(D-2*t);
    QUESTIONS.push({t:'draw',
      q:`축의 기준 지름이 ${D}mm이고 공차가 ±${t}mm일 때, 축의 최소 지름은?`,
      c:[mn+'mm', mx+'mm', D+'mm', d2+'mm'], a:0,
      e:`최소 지름 = 기준 - 공차 = ${D} - ${t} = ${mn}mm.`});
  }
})();

